// : Implement a switch statement that evaluates an expression and uses the default case if none of the cases match.

let grade = "D";

switch (grade) {
  case "A":
    console.log("Excellent");

    break;
  case "B":
    console.log("Good");

    break;
  case "c":
    console.log("Fair");

    break;
  case "D":
    console.log("Poor");

    break;
  default:
    console.log("Invalid Result");

    break;
}
